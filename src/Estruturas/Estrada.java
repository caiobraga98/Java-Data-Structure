
package Estruturas;


public class Estrada {
    
   private String nomeestrada;
   private Pilha pilha;

    public Estrada(String nomeestrada, Pilha pilha) {
        this.nomeestrada = nomeestrada;
        this.pilha = pilha;
    }

    public String getNomeestrada() {
        return nomeestrada;
    }

    public void setNomeestrada(String nomeestrada) {
        this.nomeestrada = nomeestrada;
    }

    public Pilha getPilha() {
        return pilha;
    }

    

   
   
   
    
}
